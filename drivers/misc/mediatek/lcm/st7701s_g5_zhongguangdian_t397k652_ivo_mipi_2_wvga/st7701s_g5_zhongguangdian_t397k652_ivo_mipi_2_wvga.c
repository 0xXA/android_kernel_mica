static void lcm_init(void) {
    lcm_util.set_reset_pin(0x1);
    lcm_util.mdelay(0x5);
    lcm_util.set_reset_pin(0x00);
    lcm_util.mdelay(0xA);
    lcm_util.set_reset_pin(0x1);
    lcm_util.mdelay(0x78);
    push_table(lcm_initialization_setting, sizeof(lcm_initialization_setting) / sizeof(struct LCM_setting_table), 0x1);
}

static void lcm_suspend(void) {
    push_table(lcm_deep_sleep_mode_in_setting, sizeof(lcm_deep_sleep_mode_in_setting) / sizeof(struct LCM_setting_table), 0x1);
    lcm_util.set_reset_pin(0x1);
    lcm_util.mdelay(0xA);
    lcm_util.set_reset_pin(0x00);
    lcm_util.mdelay(0x14);
    lcm_util.set_reset_pin(0x1);
    lcm_util.mdelay(0x78);
}

static void lcm_resume(void) {
    lcm_init();
}

