static void push_table(struct LCM_setting_table *table, unsigned int count, unsigned char force_update) {
  unsigned int i;

  for(i = 0x00; i < count; i++) {

    unsigned cmd;
    cmd = table[i].cmd;

    switch (cmd) {

      case 0xFFE :
        lcm_util.mdelay(table[i].count);
        break;

      case 0xFFF :
        break;

      default:
        lcm_util.dsi_set_cmdq_V2(cmd, table[i].count, table[i].para_list, force_update);
    }
  }

}

